// ロール別に表示する約款セクション一覧
export function getTermsForRole(role: string): { label: string; sections: TermsSection[] }[] {
  const result: { label: string; sections: TermsSection[] }[] = [];

  if (["artist", "organizer", "agent"].includes(role)) {
    result.push({ label: "アーティスト向け口座作成約款", sections: ARTIST_TERMS });
  }
  if (["organizer", "agent"].includes(role)) {
    result.push({ label: "オーガナイザー向け追加約款", sections: ORGANIZER_ADDITIONAL_TERMS });
  }
  if (role === "agent") {
    result.push({ label: "エージェント向け追加約款", sections: AGENT_ADDITIONAL_TERMS });
  }
  return result;
}

export function getAgreedRoles(role: string): string[] {
  const roles = ["artist"];
  if (role === "organizer" || role === "agent") roles.push("organizer");
  if (role === "agent") roles.push("agent");
  return roles;
}
